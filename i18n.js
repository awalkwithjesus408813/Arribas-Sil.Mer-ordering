/* The order languages appear in the switcher */
window.I18N_ORDER = ["EN", "JA", "ES", "ZH"];

/* ---- tiny helper used by every screen ---- */
window.t = function (key) {
  const lang = window.APP_LANG || "EN";
  return (window.I18N[lang] && window.I18N[lang][key]) || window.I18N.EN[key] || key;
};

/* Apply translations to any element carrying data-i18n / data-i18n-ph */
window.applyI18n = function (root) {
  (root || document).querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = window.t(el.getAttribute("data-i18n"));
  });
  (root || document).querySelectorAll("[data-i18n-ph]").forEach((el) => {
    el.setAttribute("placeholder", window.t(el.getAttribute("data-i18n-ph")));
  });
};
